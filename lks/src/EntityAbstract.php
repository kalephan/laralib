        $this->structure->fields = [];
        $this->structure->indelibility = [];
        $this->structure->order_by = ['id' => 'desc'];
        $this->structure->url_prefix = '';
        $this->structure->actions = [
            'view' => ['url' => '@structure_url_prefix/@id', 'title' => lks_lang('Xem')],
